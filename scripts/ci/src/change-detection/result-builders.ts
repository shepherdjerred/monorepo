/**
 * {@link AffectedPackages} result constructors for the three detection outcomes:
 * a full build, an empty (no-build) result, and a scoped result derived from a
 * transitively-closed affected-package set.
 */
import {
  ALL_PACKAGES,
  PACKAGES_WITH_IMAGES,
  PACKAGES_WITH_NPM,
  PACKAGE_TO_SITE,
} from "../catalog.ts";
import type { AffectedPackages } from "../lib/types.ts";
import {
  isAutoGeneratedCommit,
  isReleasePleaseMerge,
} from "./version-commit.ts";

/**
 * A full build rebuilds and retests everything, but a cooklang plugin release
 * is published only when cooklang *source* actually changed — `buildAll` alone
 * must never cut a release (that was the source of the manifest-bump churn).
 */
export function fullBuildResult(cooklangChanged: boolean): AffectedPackages {
  return {
    packages: new Set(ALL_PACKAGES),
    // On a full build the drift gate is skipped (every per-package job runs
    // `bun install --frozen-lockfile`), so seeds don't need to be tracked.
    directlyChanged: new Set(),
    buildAll: true,
    homelabChanged: true,
    tofuChanged: true,
    cooklangChanged,
    resumeChanged: true,
    // Full build rebuilds everything; run the drift gate too.
    helmTypesInputsChanged: true,
    ciImageChanged: false,
    hasImagePackages: new Set(PACKAGES_WITH_IMAGES),
    hasSitePackages: new Set(Object.keys(PACKAGE_TO_SITE)),
    hasNpmPackages: new Set(PACKAGES_WITH_NPM),
    ciImageVersionChanged: false,
    versionBumpOnly: false,
    releasePleaseMerge: isReleasePleaseMerge(),
    isAutoGenerated: isAutoGeneratedCommit(),
  };
}

export function emptyResult(): AffectedPackages {
  return {
    packages: new Set(),
    directlyChanged: new Set(),
    buildAll: false,
    homelabChanged: false,
    tofuChanged: false,
    cooklangChanged: false,
    resumeChanged: false,
    helmTypesInputsChanged: false,
    ciImageChanged: false,
    hasImagePackages: new Set(),
    hasSitePackages: new Set(),
    hasNpmPackages: new Set(),
    ciImageVersionChanged: false,
    versionBumpOnly: false,
    releasePleaseMerge: isReleasePleaseMerge(),
    isAutoGenerated: isAutoGeneratedCommit(),
  };
}

type ScopedResultOptions = {
  allAffected: Set<string>;
  directlyChanged: Set<string>;
  cooklangChanged: boolean;
  ciImageChanged: boolean;
  ciImageVersionChanged?: boolean;
  tofuChanged?: boolean;
  helmTypesInputsChanged?: boolean;
};

export function buildScopedResult(
  options: ScopedResultOptions,
): AffectedPackages {
  const {
    allAffected,
    directlyChanged,
    cooklangChanged,
    ciImageChanged,
    ciImageVersionChanged = false,
    tofuChanged = false,
    helmTypesInputsChanged = false,
  } = options;
  const hasImagePackages = new Set<string>();
  const hasSitePackages = new Set<string>();
  const hasNpmPackages = new Set<string>();
  for (const pkg of allAffected) {
    if (PACKAGES_WITH_IMAGES.has(pkg)) {
      hasImagePackages.add(pkg);
    }
    if (pkg in PACKAGE_TO_SITE) {
      hasSitePackages.add(pkg);
    }
    if (PACKAGES_WITH_NPM.has(pkg)) {
      hasNpmPackages.add(pkg);
    }
  }

  return {
    packages: allAffected,
    directlyChanged,
    buildAll: false,
    homelabChanged: allAffected.has("homelab"),
    tofuChanged,
    cooklangChanged,
    resumeChanged: allAffected.has("resume"),
    helmTypesInputsChanged,
    ciImageChanged,
    hasImagePackages,
    hasSitePackages,
    hasNpmPackages,
    ciImageVersionChanged,
    versionBumpOnly: false,
    releasePleaseMerge: isReleasePleaseMerge(),
    isAutoGenerated: isAutoGeneratedCommit(),
  };
}
