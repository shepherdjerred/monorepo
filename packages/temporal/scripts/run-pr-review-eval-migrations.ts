#!/usr/bin/env bun
/**
 * Apply pending pr_review_eval migrations against a connection string.
 *
 * Usage:
 *   PR_REVIEW_EVAL_DATABASE_URL=<postgres-dsn> bun run packages/temporal/scripts/run-pr-review-eval-migrations.ts
 *
 * The DSN format follows the standard libpq URI shape — placeholders are
 * shown abstractly to keep this docstring out of the no-secrets pattern
 * matcher. The temporal worker boots with the same env var sourced from
 * the pr_review_eval-credentials.temporal-postgresql.credentials.postgresql
 * Kubernetes secret (postgres-operator generates one per database user
 * automatically). Calling this script locally requires a port-forward.
 */
import { runMigrations } from "#db/migrate.ts";

async function main(): Promise<void> {
  const connectionString = Bun.env["PR_REVIEW_EVAL_DATABASE_URL"];
  if (connectionString === undefined || connectionString === "") {
    console.error("PR_REVIEW_EVAL_DATABASE_URL is required.");
    console.error(
      "  Expected format: a libpq URI (scheme + user + password + host + port + database)",
    );
    console.error(
      "  Source the value from the Kubernetes secret created by postgres-operator,",
    );
    console.error(
      "  or use a port-forward + the credentials generated by the operator on apply.",
    );
    process.exit(2);
  }

  const applied = await runMigrations(connectionString);
  if (applied.length === 0) {
    console.warn(JSON.stringify({ msg: "No pending migrations", applied }));
  } else {
    console.warn(JSON.stringify({ msg: "Applied migrations", applied }));
  }
}

async function entrypoint(): Promise<void> {
  try {
    await main();
  } catch (error: unknown) {
    console.error(error);
    process.exit(1);
  }
}

void entrypoint();
